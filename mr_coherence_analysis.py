def compute_stats(data):
    """Return a DataFrame of summary statistics per i value."""
    arr = np.array(list(data.values()))
    rows = []
    for i in range(11):
        col = arr[:, i]
        rows.append({
            'i': i,
            'n': len(col),
            'median': np.median(col),
            'Q1': np.percentile(col, 25),
            'Q3': np.percentile(col, 75),
            'IQR': np.percentile(col, 75) - np.percentile(col, 25),
            'pct_above_10': 100 * np.mean(col > 0.10),
            'pct_above_50': 100 * np.mean(col > 0.50),
        })
    return pd.DataFrame(rows)


def ell_star(stats_df, threshold=0.10):
    """First i where median drops below threshold."""
    below = stats_df[stats_df['median'] < threshold]
    if below.empty:
        return None
    return int(below.iloc[0]['i'])


def print_stats(stats_df, label):
    print(f"\n{'='*65}")
    print(f"{label}  (n={int(stats_df['n'].iloc[0])} sequences)")
    print(f"{'='*65}")
    print(f"{'i':>4}  {'median':>8}  {'Q1':>8}  {'Q3':>8}  {'IQR':>8}  "
          f"{'%>0.10':>8}  {'%>0.50':>8}")
    print(f"{'-'*65}")
    for _, row in stats_df.iterrows():
        print(f"{int(row['i']):>4}  {row['median']:>8.3f}  {row['Q1']:>8.3f}  "
              f"{row['Q3']:>8.3f}  {row['IQR']:>8.3f}  "
              f"{row['pct_above_10']:>7.1f}%  {row['pct_above_50']:>7.1f}%")
    es = ell_star(stats_df)
    prev_med = stats_df[stats_df['i'] == es - 1]['median'].values[0] if es else None
    print(f"\n  ell* (median < 0.10) = {es}  "
          f"(median at i={es-1} was {prev_med:.3f})" if es else "  ell* > 10")


def to_long(data, family, group):
    """Convert raw dict to long-format DataFrame."""
    rows = []
    for seq_id, values in data.items():
        for i, v in enumerate(values):
            rows.append({'family': family, 'group': group,
                         'seq_id': seq_id, 'i': i, 'coverage': v})
    return pd.DataFrame(rows)


